    public function approve(Post $post)
    {
        $post->update(['is_approved'=>1]);
        $admins = User::all();
        $data = ['title'=>$post->title];
        Notification::send($admins, new StatusUpdate($data));
        return response()->json(['success'=>true,'message'=>'Post Approved Successfully']);
